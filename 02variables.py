# Naming convention 
# 1. A variable name must start with a letter or the underscore character
# 2. A variable name cannot start with a number
# 3. A variable name can only contain alphanumeric characters and underscores (A-z, 0-9, and _ )
# 4. A variable name cannot be any of the Python keywords.

# There is no type of variable in python just like js
x = 10
print(x)

x = "Dishant"
print(x)

# type casting in py
a = str(3)
b = int(3)
c = float(3)

print(a, b, c)

# type() is used to check the type of variable in py
print(type(a), type(b), type(c))

# single quote and double quote mean same in py
d = 'hi'
e = "hi"
print(d == e)

